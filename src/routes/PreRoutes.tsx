import React from 'react'
import Routes from './Routes'
import usePreRoutes from './usePreRoutes'

//-------------------------------------------------------------------------------------------------
const PreRoutes: React.FC = function () {
  //---------------------------------------------------------------------------------------------
  const confirm = usePreRoutes()
  //---------------------------------------------------------------------------------------------

  //---------------------------------------------------------------------------------------------
  if (confirm === null) return <div className='pre-checking' />
  else return <Routes isLogged={confirm} />
  //---------------------------------------------------------------------------------------------
}
//-------------------------------------------------------------------------------------------------
export default PreRoutes
